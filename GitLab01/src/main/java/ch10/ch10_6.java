package ch10;

public class ch10_6 {
	public static void main(String[] args) {
		int x1 = -30;
		int x2 = 50;
		System.out.println("Absolute Value of -30 is " + Math.abs(x1));
		System.out.println("Absolute Value of 50 is " + Math.abs(x2));
		double y1 = -5.5;
		double y2 = 3.6;
		System.out.println("Absolute Value of -5.5 is " + Math.abs(y1));
		System.out.println("Absolute Value of 3.6 is " + Math.abs(y2));
	}

}
