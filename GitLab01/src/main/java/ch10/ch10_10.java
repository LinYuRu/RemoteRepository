package ch10;

public class ch10_10 {
	public static void main(String[] args) {
		double x = 4.0;
		System.out.println("sqrt(4.0) is " + Math.sqrt(x));
		x = 8.0;
		System.out.println("cbrt(8.0) is " + Math.cbrt(x));
		x = 3.0;
		System.out.println("ceil(3.0, 4) is " + Math.pow(x, 4));
		x = 2.0;
		System.out.println("exp(2.0) is " + Math.exp(x));
		x = 2.7;
		System.out.println("log(2.7) is " + Math.log(x));
		x = 10.0;
		System.out.println("log10(10) is " + Math.log10(x));
	}

}
