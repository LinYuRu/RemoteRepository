package ch10;

public class ch10_9 {
	public static void main(String[] args) {
		double x = -3.49;
		System.out.println("-3.49的 ceil() 值是 " + Math.ceil(x));
		System.out.println("-3.49的 floor() 值是 " + Math.floor(x));
		double y = 5.5;
		System.out.println("5.5的 ceil() 值是 " + Math.ceil(y));
		System.out.println("5.5的 floor() 值是 " + Math.floor(y));
	}

}
